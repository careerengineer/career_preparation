// who에 따라 적용 가능한 질문 목록 반환 (whoOnly가 있는 질문은 해당 페르소나일 때만 포함)
// who와 답변 스냅샷에 따라 적용 가능한 질문 목록 반환
// - whoOnly가 있는 질문: 해당 페르소나일 때만 포함
// - dependsOn이 있는 질문: 의존 답변이 일치할 때만 포함
function getVisibleQS(who, ansSnapshot) {
  return QS.filter(q => {
    if (q.whoOnly && who && !q.whoOnly.includes(who)) return false;
    if (q.dependsOn) {
      const parentVal = ansSnapshot && ansSnapshot[q.dependsOn.id];
      if (parentVal !== q.dependsOn.value) return false;
    }
    return true;
  });
}

